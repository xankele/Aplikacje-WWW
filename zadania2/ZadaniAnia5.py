class Calculator:
    def __init__(self, a, b):
        self.a = a
        self.b = b
    def add(self):
        return a+b
    def subtract(self):
        return a-b
    def multiply(self):
        return a*b
    def divide(self):
        return a/b
#zad6
class ScienceCalculator(Calculator):
    def power(self):
        return a**b


